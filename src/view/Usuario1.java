    /**
     * Botao para salvar o usuario registado
     * @param evt 
     */
    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        model.setNomeUsuario(jtfNome.getText());
        model.setLoginUsuario(jtfUsuario.getText());
        model.setSenhaUsuario(jtfSenha.getText());
        
        controller.salvarUsuarioController(model);
        preencherTabela();
    }//GEN-LAST:event_jbSalvarActionPerformed

    /**
     * Botao para excluir usuario selecionado da tabela
     * @param evt 
     */
    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        if(jtUsuario.getSelectedRow()>-1){
            controller.excluirUsuarioController(Integer.parseInt(String.valueOf(jtUsuario.getValueAt(jtUsuario.getSelectedRow(), 0))));
        }
        preencherTabela();
    }//GEN-LAST:event_jbExcluirActionPerformed


