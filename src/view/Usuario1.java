    /**
     * Preenche a tabela de usuarios
     */
    private void preencherTabela(){
        lista = controller.getListaUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
        modelo.setNumRows(0);
        
        for(int i=0; i<lista.size(); i++){
            modelo.addRow(new Object[]{
            lista.get(i).getIdUsuario(),
            lista.get(i).getNomeUsuario(),
            lista.get(i).getLoginUsuario()
            
            });
        }
                
    }

