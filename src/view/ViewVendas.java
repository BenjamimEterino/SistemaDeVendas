    private void jbAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jtVenda.getModel();
        if(jtProdutos.getSelectedRow()!=-1){
            modelProduto = controllerProduto.getProdutoController((int) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 0));
        } else{
            JOptionPane.showInternalMessageDialog(null, "Selecione o produto", "Aviso", 
                    JOptionPane.WARNING_MESSAGE);
        }
        

        if(Integer.parseInt(String.valueOf( jsQuantidade.getValue()))> modelProduto.getStock()){
            JOptionPane.showInternalMessageDialog(null, "Quantidade nao disponivel", 
                    "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        else{
            modelo.addRow(new Object[]{
                modelProduto.getIdProduto(),
                modelProduto.getNomeProduto(),
                jsQuantidade.getValue(),
                modelProduto.getPrecoProduto(),
                Double.parseDouble(String.valueOf(jsQuantidade.getValue()))*modelProduto.getPrecoProduto()
            });

            total+= Double.parseDouble(String.valueOf(jsQuantidade.getValue()))*modelProduto.getPrecoProduto();
            jtfPreco.setText(String.valueOf(total));
            jtfTotal.setText(String.valueOf(total));

        }

    }//GEN-LAST:event_jbAdicionarActionPerformed

