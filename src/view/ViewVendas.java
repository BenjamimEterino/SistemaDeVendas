    private void jbFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFinalizarActionPerformed
        modelVenda = new ModelVenda();

        try {
            modelVenda.setDataVenda(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
                ex.printStackTrace();
        }
        try{
            modelVenda.setDescontoVenda(Double.parseDouble(String.valueOf(jtfDesconto.getText())));
        } catch(NumberFormatException e){
            modelVenda.setDescontoVenda(0);
        }

        modelVenda.setIdCliente(Integer.parseInt(String.valueOf(jtfCodigoCliente.getText())));
        modelVenda.setValorVenda(Double.parseDouble(String.valueOf(jtfPreco.getText())));
        modelVenda.setTotalVenda(Double.parseDouble(String.valueOf(jtfTotal.getText())));

        codigoVenda = controllerVenda.salvarVendaController(modelVenda);
        if(codigoVenda > 0){
            JOptionPane.showMessageDialog(null, "Venda sucedida");
            total=0;
        } else{
            JOptionPane.showMessageDialog(null, "Erro ao vender");
        }

        int cont = jtVenda.getRowCount();
        for(int i=0; i<cont; i++){
            modelVendasProdutos = new ModelVendasProdutos();
            modelVendasProdutos.setProduto((int) jtVenda.getValueAt(i, 0));
            modelVendasProdutos.setVenda(codigoVenda);
            modelVendasProdutos.setQuantidadeVenda((int) jtVenda.getValueAt(i, 2));
            modelVendasProdutos.setValorVenda((double) jtVenda.getValueAt(i, 4));

            listaVP.add(modelVendasProdutos);
        }
        if( controllerVP.salvarVendasProdutosController(listaVP)){
            for(int i=0; i<cont; i++){
                int pos = Integer.parseInt(String.valueOf(jtVenda.getValueAt(i, 0)));
                int quant = Integer.parseInt(String.valueOf(jtVenda.getValueAt(i, 2)));
                modelProduto = controllerProduto.getProdutoController(pos);
                modelProduto.setStock(modelProduto.getStock()-quant);
                if(modelProduto.getStock()==0){
                    controllerProduto.excluirProdutoController(modelProduto.getIdProduto());
                } else{
                     controllerProduto.atualizarProdutoQuantidade(modelProduto, pos);
                }
            }
        }
        preencherProdutos();
         limparCampos();
            carregarVendas();
    }//GEN-LAST:event_jbFinalizarActionPerformed

