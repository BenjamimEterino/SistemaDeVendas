    /**
     * Preenche a tabela de clientes
     */
    private void preencherTabela(){
       listaCliente =  controller.getListaClienteController();
       DefaultTableModel modelo = (DefaultTableModel) jtCliente.getModel();
       modelo.setNumRows(0);
       
       int count = listaCliente.size();
       for (int i=0; i<count; i++){
           modelo.addRow(new Object[]{
           listaCliente.get(i).getIdCliente(),
           listaCliente.get(i).getNomeCliente(),
           listaCliente.get(i).getEnderecoCliente(),
           listaCliente.get(i).getCidadeCliente(),
           listaCliente.get(i).getTelefone()
           
           
           });
       }
       limparCampos();
    }
    
    /**
     * Limpa os campos de edicao
     */
    private void limparCampos(){
        jtfCodigo.setText("");
        tfNome.setText("");
        tfTelefone.setText("");
        tfEndereco.setText("");
        cbCidade.setSelectedIndex(0);
    }
