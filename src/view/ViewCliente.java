    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
           if(salvarAlterar.equals("salvar")){
               salvarClientes();
           } else if(salvarAlterar.equals("alterar")){
               alterarClientes();
           }
    }//GEN-LAST:event_jbSalvarActionPerformed
    
    /**
     * Salva um novo cliente
     */
    private void salvarClientes(){
        model.setNomeCliente(tfNome.getText());
        model.setEnderecoCliente(tfEndereco.getText());
        model.setTelefone(tfTelefone.getText());
        model.setCidadeCliente(cbCidade.getSelectedItem().toString());
            
        if(controller.salvarClienteController(model)>0){
            limparCampos();
            habilitarCampos(false);
            preencherTabela();
        }
    }
    
    /**
     * Altera dados do cliente
     */
    private void alterarClientes(){
        model.setNomeCliente(tfNome.getText());
        model.setEnderecoCliente(tfEndereco.getText());
        model.setTelefone(tfTelefone.getText());
        model.setCidadeCliente(cbCidade.getSelectedItem().toString());
        
        if(controller.atualizarClienteController(model)){
            JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso");
             
            preencherTabela();            
            limparCampos();
            habilitarCampos(false);
        } else{
            JOptionPane.showMessageDialog(null, "Problemas ao alterar cliente");
        }
            
        
       
        
    }
    private void jtClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClienteMouseClicked
        if(jtCliente.getSelectedRow()!=-1){
          int codigo = (int) jtCliente.getValueAt(jtCliente.getSelectedRow(), 0);
          
          try{
              
              model = controller.getClienteController(codigo);
              jtfCodigo.setText(String.valueOf(model.getIdCliente()));
              tfNome.setText(model.getNomeCliente());
              tfEndereco.setText(model.getEnderecoCliente());
              tfTelefone.setText(model.getTelefone());
              cbCidade.setSelectedItem(model.getCidadeCliente());
               jbAlterar.setEnabled(true);
               jbExcluir.setEnabled(true);
              
          } catch(Exception e){
              
          }
        }
    }//GEN-LAST:event_jtClienteMouseClicked

